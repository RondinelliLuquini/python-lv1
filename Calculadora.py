num1 = float(input ("Digte um numero: "))
num2 = float(input ("Digte outro numero: "))
operacao = input ("Selecione a operação: + Soma, - Subtracao , * Multiplicacao, / Divisao: ")

if operacao == "+":
    print(num1 + num2)
elif operacao == "-":
    print(num1 + num2)
elif operacao == "*":
    print(num1 * num2)
elif operacao == "/":
    print(num1 / num2)

else:
    print("operacao invalida")



